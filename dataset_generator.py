# Generic slides if domain template is missing
GENERIC_SLIDES = [
    {"title": "مقدمة", "content": ["تعريف {topic}", "الأهداف"]},
    {"title": "العناصر الأساسية", "content": ["العنصر الأول", "العنصر الثاني"]},
    {"title": "المميزات والعيوب", "content": ["الإيجابيات", "السلبيات"]},
    {"title": "الخاتمة", "content": ["ملخص سريع", "أسئلة ومناقشة"]}
]

def generate_example(domain, topic):
    pattern_idx = random.randint(0, len(PATTERNS) - 1)
    
    input_text = PATTERNS[pattern_idx].format(topic=topic)
    formal_text = FORMAL_PATTERNS[pattern_idx].format(topic=topic)
    
    slides_template = SLIDE_TEMPLATES.get(domain, GENERIC_SLIDES)
    slides = []
    num_slides = random.randint(3, 5)
    
    for i in range(min(num_slides, len(slides_template))):
        s = slides_template[i]
        slides.append({
            "title": s["title"].format(topic=topic),
            "content": [c.format(topic=topic) for c in s["content"]]
        })
    
    difficulty = random.choice(["easy", "medium", "advanced"])
    keywords = [topic, domain.split(" & ")[0]]
    
    return {
        "input": input_text,
        "formal": formal_text,
        "topic": topic,
        "slides": slides,
        "difficulty": difficulty,
        "keywords": keywords
    }

def generate_dataset(num_examples=500):
    dataset = []
    domains_list = list(DOMAINS.keys())
    
    for _ in range(num_examples):
        domain = random.choice(domains_list)
        topic = random.choice(DOMAINS[domain])
        example = generate_example(domain, topic)
        dataset.append(example)
    
    return dataset

if __name__ == "__main__":
    data = generate_dataset(500)
